public class Spikey {
    public static void top() {
        System.out.println("  \\/");
        System.out.println(" \\\\//");
        System.out.println("\\\\\\///");
    }
    public static void bottom() {
        System.out.println("///\\\\\\");
        System.out.println(" //\\\\");
        System.out.println("  /\\");
    }
    public static void main(String[] args) {
        top();
        bottom();
    }
}
